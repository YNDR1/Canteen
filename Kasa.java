    private void IslemSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IslemSilActionPerformed
        try {
         int a = JOptionPane.showConfirmDialog(null,"İşlem silinsin mi ? ", "Uyarı", JOptionPane.WARNING_MESSAGE);
         if(a == 0){
            String tarih = (String) dtm.getValueAt(tabloKasa.getSelectedRow(), 0);
            String tur = (String) dtm.getValueAt(tabloKasa.getSelectedRow(), 1);
            String aciklama = (String) dtm.getValueAt(tabloKasa.getSelectedRow(), 2);
         
                if (k.kasaIslemSil(tarih, tur, aciklama)) {
                dtm.removeRow(tabloKasa.getSelectedRow());
                }
                listele();
            toplamFiyatlar();
         }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Listeden İşlem Seçiniz", " Uyarı", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_IslemSilActionPerformed

