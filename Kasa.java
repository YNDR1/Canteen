    public void listele() {
          if (dtm.getRowCount() > 0) {
            for (int i = dtm.getRowCount() - 1; i > -1; i--) {
                dtm.removeRow(i);
            }
        }
        for (int i = 0; i < k.kasaIslemler.size(); i++) {
            dtm.addRow(new String[]{k.kasaIslemler.get(i).tarih, k.kasaIslemler.get(i).tur, k.kasaIslemler.get(i).aciklama, String.valueOf(k.kasaIslemler.get(i).para)});
        }
    }

    public void toplamFiyatlar() {
        toplamAlacak=0;
        toplamBakiye=0;
        bulunanToplamPara = 0;
        for (int i = 0; i < k.musteriler.size(); i++) {
            if (k.musteriler.get(i).bakiye < 0) {
                toplamAlacak -= k.musteriler.get(i).bakiye;
            } else {
                toplamBakiye += k.musteriler.get(i).bakiye;
            }
        }
        
        for (int i = 0; i < k.kasaIslemler.size(); i++) {
            if (k.kasaIslemler.get(i).tur.equals("giris")) {
                bulunanToplamPara += k.kasaIslemler.get(i).para;
            } else {
                bulunanToplamPara += k.kasaIslemler.get(i).para;
            }
        }
          labelToplamPara.setText(String.valueOf(bulunanToplamPara));
        labelToplamAlacak.setText(String.valueOf(toplamAlacak));
        labelToplamBakiye.setText(String.valueOf(toplamBakiye));
    }

    
